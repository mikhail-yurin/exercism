export const twoFer = (name) => {
  switch (name) {
      case 'Alice': {
        return 'One for Alice, one for me.';
      }
      case 'Bob': {
        return 'One for Bob, one for me.';
      }
      default: {
        return 'One for you, one for me.';
      }
  }
};
